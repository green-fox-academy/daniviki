import java.util.Scanner;
public class MultiplicationTable {
        public static void main(String[] args) {
            Scanner scanner = new Scanner(System.in);
            System.out.println("Add a number: ");
            int addNum = scanner.nextInt();

            System.out.println("1 * " + addNum + " = " + 1 * addNum);
            System.out.println("2 * " + addNum + " = " + 2 * addNum);
            System.out.println("3 * " + addNum + " = " + 3 * addNum);
            System.out.println("4 * " + addNum + " = " + 4 * addNum);
            System.out.println("5 * " + addNum + " = " + 5 * addNum);
            System.out.println("6 * " + addNum + " = " + 6 * addNum);
            System.out.println("7 * " + addNum + " = " + 7 * addNum);
            System.out.println("8 * " + addNum + " = " + 8 * addNum);
            System.out.println("9 * " + addNum + " = " + 9 * addNum);
            System.out.println("10 * " + addNum + " = " + 10 * addNum);





            // Create a program that asks for a number and prints the multiplication table with that number
            //
            // Example:
            // The number 15 should print:
            //
            // 1 * 15 = 15
            // 2 * 15 = 30
            // 3 * 15 = 45
            // 4 * 15 = 60
            // 5 * 15 = 75
            // 6 * 15 = 90
            // 7 * 15 = 105
            // 8 * 15 = 120
            // 9 * 15 = 135
            // 10 * 15 = 150
        }
}
